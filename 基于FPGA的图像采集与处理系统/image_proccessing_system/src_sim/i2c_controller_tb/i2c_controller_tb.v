// ---------------------------------------------------------------------------------
// 模块定义 Module Define
// --------------------------------------------------------------------------------- 
    module i2c_controller_tb;

// ---------------------------------------------------------------------------------
// 局部常量 Local Constant Parameters
// ---------------------------------------------------------------------------------
    localparam   T = 10;                             //系统时钟周期, 100MHz

    localparam   SLAVE_ADDR    = 7'h3c;              //OV5640的器件地址为7'h3c
    localparam   BIT_CTRL      = 1'b1;               //OV5640的字节地址为16位, 8位(0)/16位(1)
    localparam   CLK_FREQ      = 27'd100_000_000;    //i2c_controller模块的驱动时钟频率
    localparam   I2C_FREQ      = 18'd250_000;        //I2C的SCL时钟频率, 不超过400KHz

// ---------------------------------------------------------------------------------
// 模块内变量定义 Module_Variables
// --------------------------------------------------------------------------------- 
    // output signal
    reg             sys_clk_100m;
    reg             sys_rst_n;
    reg             i2c_rw_ctrl;
    reg             i2c_exec;
    reg    [15 : 0] i2c_addr;
    reg    [ 7 : 0] i2c_wr_data;
    
    
    // input signal
    wire   [ 7 : 0] i2c_rd_data;   
    wire            i2c_done;
    wire            scl;
    wire            sda;
    wire            i2c_dri_clk;
    
// ---------------------------------------------------------------------------------
// 数据流描述 Continuous Assignments
// --------------------------------------------------------------------------------- 


// ---------------------------------------------------------------------------------
// 结构化描述 Moudle Instantiate
// ---------------------------------------------------------------------------------
    // IIC 驱动模块
    i2c_controller
    #(
        // parameter passing
        .SLAVE_ADDR             (SLAVE_ADDR),
        .CLK_FREQ               (CLK_FREQ),
        .I2C_FREQ               (I2C_FREQ)
    )
    U_i2c_controller
    (
        // clock & reset
        .clk                    (sys_clk_100m),
        .rst_n	                (sys_rst_n),

        // i2c interface
        .i2c_exec               (i2c_exec),
        .bit_ctrl               (BIT_CTRL),
        .i2c_rw_ctrl            (i2c_rw_ctrl),
        .i2c_addr               (i2c_addr),
        .i2c_wr_data            (i2c_wr_data),
        .i2c_rd_data            (i2c_rd_data),
        .i2c_done               (i2c_done),
        .scl                    (scl),
        .sda                    (sda),

        // user interface
        .clk_dri                (i2c_dri_clk)
    );

// ---------------------------------------------------------------------------------
// 行为描述 Clocked Assignments
// ---------------------------------------------------------------------------------
    initial
    begin
        // Initialize Inputs
                sys_clk_100m = 1'b0;
                sys_rst_n    = 1'b0;
                i2c_rw_ctrl  = 1'b0;    //写
                i2c_exec     = 1'b0;
                i2c_addr     = 16'h3008;
                i2c_wr_data  = 8'h82;
        
        // Wait for global reset to finish
        #(T*5)  sys_rst_n   = 1'b1;
        #(T*10) i2c_exec    = 1'b1;
        
        // Add stimulus here
        
        // Finish
        //#(T*10000) $stop;
    end
    
    // Generate global clk
    always #(T/2) sys_clk_100m = ~sys_clk_100m;
    
// ---------------------------------------------------------------------------------
// 任务定义 Called Tasks
// ---------------------------------------------------------------------------------
    
	
// ---------------------------------------------------------------------------------
// 函数定义 Called Functions
// ---------------------------------------------------------------------------------

    
endmodule 